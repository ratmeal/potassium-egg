[BITS 64]
global page_fault_asm
global divide_by_zero_asm
global double_fault_asm
global bound_range_asm
extern page_fault
extern divide_error
extern double_fault_handler
extern bound_range_exceeded
page_fault_asm:
    ; get error code from stack
    ; save all registers
    push rax
    push rbx
    push rcx
    push rdx
    push rsi
    push rdi
    push rbp
    push r8
    push r9
    push r10
    push r11
    push r12
    push r13
    push r14
    push r15
    mov rdi, rsp
    call page_fault
    ; return to caller
    pop r15
    pop r14
    pop r13
    pop r12
    pop r11
    pop r10
    pop r9
    pop r8
    pop rbp
    pop rdi
    pop rsi
    pop rdx
    pop rcx
    pop rbx
    pop rax
    add rsp, 8 ; this makes the stack pointer rsp skip the error code on the stack
    iretq
